package com.ahmad.reelzaty.CustomObjects;

public class SplashObjects {
    int imgRes;
    String txt;

    public SplashObjects(int imgRes, String txt) {
        this.imgRes = imgRes;
        this.txt = txt;
    }

    public int getImgRes() {
        return imgRes;
    }

    public void setImgRes(int imgRes) {
        this.imgRes = imgRes;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }
}
